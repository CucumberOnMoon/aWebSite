def make_title(n):
    """Build a natural Chinese joke title of 16-50 characters."""
    base = random.choice(title_templates)
    # Add variation suffix
    suffix_variants = [
        f'（第{n+1}期）', f' 第{n+1}篇', f' 系列之{n+1}',
        f'·故事{n+1}', f' 精选{n+1}', f' 合集{n+1}',
        f'（续{n+1}）', f' 经典版第{n+1}回',
    ]
    suffix = random.choice(suffix_variants)
    title = base + suffix
    if len(title) > 50:
        title = title[:47] + '...'
    while len(title) < 16:
        title = title + suffix
    return title

